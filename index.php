            encourages the recovery process in all aspects will greatly reduce the risk of a relapse. Providing residents with healthy rules
            and guidelines to live by will bring about the necessary life style changes that promote permanent sobriety. Staff supervision
            of the recovery process will keep residents on the right track and develop a positive, recovery oriented community that 
            will benefit all individuals involved. Being a strongly structured and supervised sober living environment, any behaviors
            that contradict our vision of sobriety will not be tolerated.
